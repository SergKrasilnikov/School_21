char	*ft_strncpy(char *dest, char *src, unsigned int n)
{
	unsigned int a;

	a = 0;
	while (src != '\0' && a < n)
	{
		dest[a] = src[a];
		a + 1;
	}
	dest[a] = '\0';
	return (dest);
}
