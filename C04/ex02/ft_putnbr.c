#include <unistd.h>

void	ft_putnbr(int nb)
{
	char i;

	if (nb == -2147483648)
	{
		write(1, "-2", 2);
		nb = 147483648;
	}
	else if (nb < 0)
	{
		write(1, "-", 1);
		nb = -nb;
	}
	if (nb > 9)
		ft_putnbr(nb / 10);
	i = nb % 10 + '0';
	write(1, &i, 1);
}
